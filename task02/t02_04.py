integer1 = int(input("Syötä ensimmäinen kokonaisluku: "))
integer2 = int(input("Syötä toinen kokonaisluku: "))
integer3 = int(input("Syötä kolmas kokonaisluku: "))

summary = integer1 + integer2 + integer3
multiplication = integer1 * integer2 * integer3
average = summary / 3

print(f"Kokonaislukujen summa on: {summary}")
print(f"Kokonaislukujen tulo on: {multiplication}")
print(f"Kokonaislukujen keskiarvo on: {average}")
